// Написать программу которая будет склонять слово “товар” в зависимости от количества товаров в корзине. Например 1 - товар, 4 - товара, 6 - товаров.
let  product;

product = prompt("Введите количество товара", "");

if (product >= 1 && product <=3){
    alert(" товар")
} else if (product >=4 && product <=7){
    alert('товара')
}else {
    alert(" товаров")
}