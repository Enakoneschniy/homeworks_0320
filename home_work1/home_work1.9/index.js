// Создайте программу, которая выводит надбавку за стаж. Пользователь вводит стаж работы, а программа пишет ему надбавку.

let  work;

work = parseFloat(prompt("Введите стаж работы", ""));

if (work >= 0 && work <= 3){
    alert("Надбавка 0%")
} else if (work >= 3 && work <= 10){
    alert("Надбавка 10%")
} else if (work >= 10 && work <= 20){
    alert("Надбавка 20%")
} else {
    alert("Надбавка 25%")
}
